/**
 * Define as cores das peças no jogo de xadrez (Brancas ou Pretas).
 */
public enum Color {
    /**
     * Cor Branca (geralmente inicia a partida e joga na parte inferior do tabuleiro).
     */
    WHITE,

    /**
     * Cor Preta (joga na parte superior do tabuleiro).
     */
    BLACK
}

